#!/usr/bin/env node
/**
 * Regenerate src/data/questions-meta.ts from questions.json.
 * Run after any change to questions.json:
 *   node scripts/generate-questions-meta.mjs
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const QUESTIONS_PATH = path.join(__dirname, '../src/data/questions.json');
const OUT_PATH = path.join(__dirname, '../src/data/questions-meta.ts');

const questions = JSON.parse(fs.readFileSync(QUESTIONS_PATH, 'utf8'));

const byCategory = {};
const byDifficulty = {};
const matrix = {};

for (const q of questions) {
  byCategory[q.category] = (byCategory[q.category] || 0) + 1;
  byDifficulty[q.difficulty] = (byDifficulty[q.difficulty] || 0) + 1;
  const k = `${q.category}|${q.difficulty}`;
  matrix[k] = (matrix[k] || 0) + 1;
}

const fmtRecord = (obj) =>
  Object.entries(obj)
    .map(([k, v]) => `  '${k}': ${v},`)
    .join('\n');

const out = `/**
 * Lightweight metadata about the question pool.
 * Import this in client components that only need counts
 * to avoid pulling the full ~300KB questions.json into the bundle.
 *
 * AUTO-GENERATED by scripts/generate-questions-meta.mjs — do not edit by hand.
 */
import type { Category, Difficulty } from '@/types/question';

export const QUESTION_COUNT = ${questions.length};

export const COUNTS_BY_CATEGORY: Record<Category, number> = {
${fmtRecord(byCategory)}
};

export const COUNTS_BY_DIFFICULTY: Record<Difficulty, number> = {
${fmtRecord(byDifficulty)}
};

const MATRIX: Record<string, number> = {
${fmtRecord(matrix)}
};

/** Count questions matching optional category and difficulty filters. */
export function countQuestions(
  category: Category | null,
  difficulty: Difficulty | null
): number {
  if (!category && !difficulty) return QUESTION_COUNT;
  if (category && !difficulty) return COUNTS_BY_CATEGORY[category] ?? 0;
  if (!category && difficulty) return COUNTS_BY_DIFFICULTY[difficulty] ?? 0;
  return MATRIX[\`\${category}|\${difficulty}\`] ?? 0;
}
`;

fs.writeFileSync(OUT_PATH, out);
console.log(`Wrote ${OUT_PATH} (${questions.length} questions)`);
